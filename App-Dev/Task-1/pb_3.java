// 3. Check Even or Odd
// Problem: Write a Java program to check whether a number is even or odd.

public class pb_3 {
    public static void main(String[] args) {
        int num = 7;
        if(num%2 == 0){
            System.out.println("Even");
        }
        else{
            System.out.println("Odd");
        }
    }
}
