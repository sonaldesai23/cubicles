// 9. Print Multiplication Table
// Problem: Write a Java program to print the multiplication table of a given number.

public class pb_9 {
    public static void main(String[] args) {
        int n = 5;
        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " x " + i + " = " + n * i);
        }
    }
}
